import jwt from "jsonwebtoken";

const generateRefreshToken = function(){
  //long lived token
  return jwt.sign(
    {
      _id: this._id,
      email: this.email,
      username: this.username,
    },
    process.env.REFRESH_TOKEN_SECRET,
    {
      expiresIn: process.env.REFRESH_TOKEN_EXPIRY || "10d",
    }
  );
}
export default generateRefreshToken;